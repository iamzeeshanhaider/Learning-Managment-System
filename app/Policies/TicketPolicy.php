    /**
     * Determine whether the user can view the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Ticket  $ticket
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function view(User $user, Ticket $ticket)
    {
        return $user->isAdmin() || ($user->isStudent() && $user->id === $ticket->user_id) || ($user->isInstructor() && $user->id === $ticket->instructor_id);
    }

    /**
     * Determine whether the user can comment on the model.
     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\Ticket  $ticket
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function comment(User $user, Ticket $ticket)
    {
        return strtolower($ticket->status) === 'open' && $this->view($user, $ticket);
    }

